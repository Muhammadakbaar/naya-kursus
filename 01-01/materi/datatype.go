package main

import "fmt"

func main() {
	var angka1 uint = 1
	var angka2 int = 1
	var angka3 float32 = 1.4
	var angka4 bool = true
	var angka5 string = "SiTamvan"

	fmt.Println(" data type integer = ", angka1, angka2)
	fmt.Println(" data type float = ", angka3)
	fmt.Println(" data type boolean = ", angka4)
	fmt.Println(" data type string = ", angka5)

}
